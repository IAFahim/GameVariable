namespace Variable.Curve;

/// <summary>
/// Defines the type of easing function to apply.
/// Based on standard Robert Penner easing equations.
/// </summary>
public enum EaseType
{
    /// <summary>Linear interpolation (no easing).</summary>
    Linear,
    /// <summary>Sine In.</summary>
    InSine,
    /// <summary>Sine Out.</summary>
    OutSine,
    /// <summary>Sine InOut.</summary>
    InOutSine,
    /// <summary>Quad In.</summary>
    InQuad,
    /// <summary>Quad Out.</summary>
    OutQuad,
    /// <summary>Quad InOut.</summary>
    InOutQuad,
    /// <summary>Cubic In.</summary>
    InCubic,
    /// <summary>Cubic Out.</summary>
    OutCubic,
    /// <summary>Cubic InOut.</summary>
    InOutCubic,
    /// <summary>Quart In.</summary>
    InQuart,
    /// <summary>Quart Out.</summary>
    OutQuart,
    /// <summary>Quart InOut.</summary>
    InOutQuart,
    /// <summary>Quint In.</summary>
    InQuint,
    /// <summary>Quint Out.</summary>
    OutQuint,
    /// <summary>Quint InOut.</summary>
    InOutQuint,
    /// <summary>Expo In.</summary>
    InExpo,
    /// <summary>Expo Out.</summary>
    OutExpo,
    /// <summary>Expo InOut.</summary>
    InOutExpo,
    /// <summary>Circ In.</summary>
    InCirc,
    /// <summary>Circ Out.</summary>
    OutCirc,
    /// <summary>Circ InOut.</summary>
    InOutCirc,
    /// <summary>Back In.</summary>
    InBack,
    /// <summary>Back Out.</summary>
    OutBack,
    /// <summary>Back InOut.</summary>
    InOutBack,
    /// <summary>Elastic In.</summary>
    InElastic,
    /// <summary>Elastic Out.</summary>
    OutElastic,
    /// <summary>Elastic InOut.</summary>
    InOutElastic,
    /// <summary>Bounce In.</summary>
    InBounce,
    /// <summary>Bounce Out.</summary>
    OutBounce,
    /// <summary>Bounce InOut.</summary>
    InOutBounce
}
