namespace Variable.Motion;

/// <summary>
///     Defines the easing function to use for interpolation.
/// </summary>
public enum EasingType
{
    /// <summary>Linear interpolation (no easing).</summary>
    Linear,
    /// <summary>Quadratic in.</summary>
    QuadIn,
    /// <summary>Quadratic out.</summary>
    QuadOut,
    /// <summary>Quadratic in/out.</summary>
    QuadInOut,
    /// <summary>Cubic in.</summary>
    CubicIn,
    /// <summary>Cubic out.</summary>
    CubicOut,
    /// <summary>Cubic in/out.</summary>
    CubicInOut,
    /// <summary>Quartic in.</summary>
    QuartIn,
    /// <summary>Quartic out.</summary>
    QuartOut,
    /// <summary>Quartic in/out.</summary>
    QuartInOut,
    /// <summary>Quintic in.</summary>
    QuintIn,
    /// <summary>Quintic out.</summary>
    QuintOut,
    /// <summary>Quintic in/out.</summary>
    QuintInOut,
    /// <summary>Sinusoidal in.</summary>
    SineIn,
    /// <summary>Sinusoidal out.</summary>
    SineOut,
    /// <summary>Sinusoidal in/out.</summary>
    SineInOut,
    /// <summary>Exponential in.</summary>
    ExpoIn,
    /// <summary>Exponential out.</summary>
    ExpoOut,
    /// <summary>Exponential in/out.</summary>
    ExpoInOut,
    /// <summary>Circular in.</summary>
    CircIn,
    /// <summary>Circular out.</summary>
    CircOut,
    /// <summary>Circular in/out.</summary>
    CircInOut,
    /// <summary>Back in.</summary>
    BackIn,
    /// <summary>Back out.</summary>
    BackOut,
    /// <summary>Back in/out.</summary>
    BackInOut,
    /// <summary>Elastic in.</summary>
    ElasticIn,
    /// <summary>Elastic out.</summary>
    ElasticOut,
    /// <summary>Elastic in/out.</summary>
    ElasticInOut,
    /// <summary>Bounce in.</summary>
    BounceIn,
    /// <summary>Bounce out.</summary>
    BounceOut,
    /// <summary>Bounce in/out.</summary>
    BounceInOut
}
